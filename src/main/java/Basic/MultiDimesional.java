package Basic;

public class MultiDimesional {

	public static void main(String[] args)
	{
		int a[][]=new int[2][2];
		a[0][0]=1;
		a[0][1]=2;
		a[1][0]=1;
		a[1][1]=2;
		
			
		
		
		
		System.out.println(a[0][0]);
		System.out.println(a[0][1]);
		System.out.println(a[1][0]);
		System.out.println(a[1][1]);
		
		
	

	}
	}

